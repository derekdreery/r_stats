fn main() {
    let c_test = OsString::from("c");
    cc::Build::new()
        .define("MATHLIB_STANDALONE", "1")
        .include("R/nmath")
        .include("R/include")
        .include("R/config")
        .files(fs::read_dir("R/nmath").unwrap().filter_map(|entry| {
            let path = entry.unwrap().path();
            if path.extension() == Some(c_test.as_os_str()) {
                Some(path)
            } else {
                None
            }
        }))
        .warnings(false)
        .compile("nmath");

    let mut builder = bindgen::Builder::default()
        .header("wrapper.h")
        .parse_callbacks(Box::new(bindgen::CargoCallbacks));
    for fn_name in FN_NAMES.iter() {
        builder = builder.whitelist_function(fn_name);
    }
    let bindings = builder.generate().expect("Unable to generate bindings");
    let out_path = PathBuf::from(env::var("OUT_DIR").unwrap());
    bindings
        .write_to_file(out_path.join("bindings.rs"))
        .expect(&format!("Cannot write to OUT_DIR {}", out_path.display()));
}
